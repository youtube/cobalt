# ==============================================================================
# Usage: merge_rlibs_tvos_archive.sh [build_out_dir]
#
# This script is designed to be executed AFTER the "cobalt_archive" target has
# been successfully compiled by Ninja. The cobalt_archive target generates
# "obj/cobalt/libcobalt_archive.a" containing C++ objects, but leaves compiled
# Rust dependencies (application crates and Rust standard library) as separate
# .rlib or .a files under the build output directory.
#
# This script:
# 1. Navigates to the build output directory (passed as an argument or defaults
#    to the current directory).
# 2. Dynamically queries "build.ninja" to extract all compiled Rust library
#    paths (avoiding a hardcoded list that could become stale).
# 3. Merges the C++ "libcobalt_archive.a" and all detected Rust libraries
#    into a single unified static library "cobalt.a" using "libtool -static".
# ==============================================================================

set -euo pipefail

# Determine the build output directory (defaults to current directory if not specified)
OUT_DIR="${1:-.}"

if [ ! -d "${OUT_DIR}" ]; then
  echo "Error: Directory '${OUT_DIR}' does not exist."
  exit 1
fi

cd "${OUT_DIR}"

if [ ! -f "build.ninja" ]; then
  echo "Error: build.ninja not found in '${OUT_DIR}'."
  echo "This script must be run inside or pointed to the GN/Ninja build output directory."
  exit 1
fi

COBALT_ARCHIVE="obj/cobalt/libcobalt_archive.a"
if [ ! -f "${COBALT_ARCHIVE}" ]; then
  echo "Error: Base archive '${COBALT_ARCHIVE}' not found."
  echo "Make sure the 'cobalt_archive' target was built successfully first."
  exit 1
fi

echo "Scanning build.ninja for compiled Rust libraries..."

# Dynamically extract all Rust libraries (.rlib and .a) generated by rust_rlib or rust_staticlib rules
# Add '|| true' to prevent grep from exiting with 1 and crashing the script under 'set -e' when no Rust libraries are found
RUST_LIBS=$(grep -E '^build obj/.*: rust_(rlib|staticlib)' build.ninja | cut -d':' -f1 | awk '{print $2}' | sort -u || true)

EXISTING_RUST_LIBS=()
for lib in ${RUST_LIBS}; do
  if [ -f "${lib}" ]; then
    EXISTING_RUST_LIBS+=("${lib}")
  else
    echo "Warning: Target Rust library '${lib}' not found on disk, skipping."
  fi
done

if [ ${#EXISTING_RUST_LIBS[@]} -eq 0 ]; then
  echo "No Rust libraries found to merge."
  echo "Copying original archive to cobalt.a..."
  cp "${COBALT_ARCHIVE}" cobalt.a
  exit 0
fi

echo "Merging C++ archive and Rust libraries into cobalt.a..."
libtool -static -o cobalt.a "${COBALT_ARCHIVE}" "${EXISTING_RUST_LIBS[@]}"

echo "Success: Created merged static library at '$(pwd)/cobalt.a'"
