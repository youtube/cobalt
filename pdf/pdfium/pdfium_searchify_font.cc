std::vector<uint8_t> CreateCidToGidMap() {
  std::vector<uint8_t> arr;
  constexpr int kCIDToGIDMapSize = 2 * (1 << 16);
  for (int i = 0; i < kCIDToGIDMapSize; i++) {
    arr.push_back((i % 2) ? 1 : 0);
  }
  return arr;
}

