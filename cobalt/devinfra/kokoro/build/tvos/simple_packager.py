"""Simple Darwin packager, stopgap until merged into GN."""

import os
import shutil
import subprocess
import sys

src_dir = sys.argv[1]  # src directory
out_dir = sys.argv[2]  # out directory generated by ninja
package_dir = sys.argv[3]  # package directory to be generated
build_info_path = sys.argv[4]  # path of build_info.json

# Creates Darwin package directory

# Use ninja to get the inputs and outputs of the Cobalt target.
# Get only lines between 'input:' and 'outputs:'.
# Get only files in the obj/ subdirectory which end with '.a' or '.o'.
# Omit the starboard_platform_entrypoint library (which contains main()).
output = subprocess.run(['ninja', '-t', 'query', 'cobalt'],
                        capture_output=True,
                        cwd=out_dir,
                        check=False,
                        encoding='utf-8',
                        text=True).stdout
output = output[output.find('input:'):output.find('outputs:')].strip()
object_files_list = [f.strip() for f in output.split('\n')]
object_files_list = [f for f in object_files_list if f.startswith('obj/')]
object_files_list = [
    f for f in object_files_list if f.endswith('.a') or f.endswith('.o')
]
object_files_list = [
    f for f in object_files_list if '/starboard_platform_entrypoint/' not in f
]
object_files = os.path.join(out_dir, 'object_files.txt')
with open(object_files, 'w', encoding='utf-8') as f:
  f.write('\n'.join(object_files_list))

# Perform partial linking to reduce the size of the archive. Keep all
# Objective-C symbols since one of them will be used by the entrypoint.
subprocess.run([
    'ld', '-o', 'cobalt.o', '-bitcode_bundle', '-ObjC', '-r', '-filelist',
    object_files
],
               cwd=out_dir,
               check=False)
os.remove(object_files)
subprocess.run(['libtool', '-static', '-o', 'cobalt.a', 'cobalt.o'],
               cwd=out_dir,
               check=False)
os.remove(os.path.join(out_dir, 'cobalt.o'))

# Copy 'src' files (specifically for YtIntent source files)
os.makedirs(os.path.join(package_dir, 'src'), exist_ok=True)
shutil.copytree(
    os.path.join(src_dir, 'starboard', 'shared', 'uikit', 'intent', 'YtIntent'),
    os.path.join(package_dir, 'src', 'YtIntent'))

# Copy 'lib' files (cobalt.a and the YtIntent.framework tree)
os.makedirs(os.path.join(package_dir, 'lib'), exist_ok=True)
shutil.copy2(
    os.path.join(out_dir, 'cobalt.a'),
    os.path.join(package_dir, 'lib', 'cobalt.a'))
os.remove(os.path.join(out_dir, 'cobalt.a'))
# shutil.copytree(
#     os.path.join(out_dir, 'install', 'cobalt.app', 'Frameworks',
#                  'YtIntent.framework'),
#     os.path.join(package_dir, 'lib', 'YtIntent.framework'))

# Copy 'content' tree
shutil.copytree(
    os.path.join(out_dir, 'install', 'usr', 'share', 'cobalt'),
    os.path.join(package_dir, 'content'))

# Copy build_info.json
shutil.copy2(build_info_path, os.path.join(package_dir, 'build_info.json'))
